    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try {
            app = (appinterface) Naming.lookup("rmi://localhost:1040/assignment");
        } catch (NotBoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            
            ArrayList<String> empData = app.getEmpData(app.getLoggedID());
            UserNamejLabel1.setText(empData.get(1)+" "+empData.get(2));
            ID.setText(empData.get(0));
            first.setText(empData.get(1));
            last.setText(empData.get(2));
            phone.setText(empData.get(3));
            dept.setText(empData.get(4));
            email.setText(empData.get(5));
            username.setText(empData.get(6));
            password.setText(empData.get(7));
            bankNo.setText(empData.get(8));
            bankID.setText(empData.get(9));
            salary.setText(empData.get(10));
            hourly.setText(Integer.toString(Integer.parseInt(empData.get(10))/160));
            
            overtime.setText(Integer.toString(app.getOvertimeEmp(empData.get(0))));
            other.setText(Integer.toString(app.getOtherEmp(empData.get(0))));
            absent.setText(empData.get(11));
            unpaid.setText(empData.get(12));
            
            EmNameShown_Jlabel.setText(empData.get(1)+" "+empData.get(2));
            EmID_Jlabel.setText(empData.get(0));
            LocalDate currentDate = LocalDate.now();
            MonthShown_Jlabel.setText(String.valueOf(currentDate.getMonth()));
            YearShown_jlabel.setText(Integer.toString(currentDate.getYear()));
            MTDRateShown.setText(app.getMtd().substring(3));
            KWSPRateShown.setText(app.getKwsp().substring(2));
            OvertimeRatio.setText(app.getOvertime());
            absentratio.setText(app.getAbsent());
            
            float sal = Float.parseFloat(empData.get(10));
            float hour = Float.parseFloat(empData.get(10))/160;
            GrossPayShown.setText(Float.toString(sal));
            float kwsp = sal*Float.parseFloat(app.getKwsp());
            KWSPAmountShown.setText(Float.toString(kwsp));
            float mtd = sal*Float.parseFloat(app.getMtd());
            AmountofMTDshown.setText(Float.toString(mtd));
            float deductions = -(kwsp+mtd);
            TotalRequiredDeductions.setText("("+Float.toString(deductions)+")");
            float unp = (Float.parseFloat(empData.get(12))*hour*8)*Float.parseFloat(app.getAbsent());
            UnpaidLeaveShown.setText(Float.toString(unp));
            float abs = (Float.parseFloat(empData.get(11))*hour*8)*Float.parseFloat(app.getAbsent());
            AbsentWoLeave.setText(Float.toString(abs));
            float totad = -(unp+abs);
            TotalAdditonalReduction.setText("("+Float.toString(totad)+")");
            float over = app.getOvertimeClaims(empData.get(0))*hour;
            OvertimeAmount.setText(Float.toString(over));
            float other = app.getOtherClaims(empData.get(0))*hour;
            OthersAmount.setText(Float.toString(other));
            float totc = over+other;
            totClaim.setText(Float.toString(totc));
            float net = sal+deductions+totad+totc;
            NetPayAmount.setText(Float.toString(net));
            
        } catch (RemoteException ex) {
            Logger.getLogger(EmployeeHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowActivated

